    switch(countryFromURL){
        case 'United States':
            return 'US';
        case 'United Kingdom':
            return 'UK';
        case 'Australia':
            return 'AU';
        case 'New Zealand':
            return 'NZ';
        case 'Germany':
            return 'DE';
        case 'Austria':
            return 'AT';
        case 'France':
            return 'FR';
        case 'Italy':
            return 'IT';
        case 'Spain':
            return 'ES';
        case 'Denmark':
            return 'DN';
        default:
            return null;
    }
